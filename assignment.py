# Rectangle calculations
length = 48   # in cm
width = 25    # in cm

area = length * width
perimeter = 2 * (length + width)

print("Area of rectangle:", area)
print("Perimeter of rectangle:", perimeter)

# BMI calculation
weight = 78      # in kilograms
height = 1.75    # in meters

bmi = weight / (height ** 2)

print("BMI is:", bmi)
