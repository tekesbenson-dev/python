# Task 1: Using a for loop to print from 2000 to 2024
print("Task 1: Years from 2000 to 2024")
for year in range(2000, 2025):
    print(year)

print("\n----------------------\n")

# Task 2: Create a list of colors and loop through using a for loop
print("Task 2: List of Colors")
colors = ["Blue", "Green", "Red", "Pink", "Black"]

for color in colors:
    print(color)

print("\n----------------------\n")

# Task 3: Using a while loop to print from 20 to 1
print("Task 3: Numbers from 20 to 1")
number = 20

while number >= 1:
    print(number)
    number -= 1